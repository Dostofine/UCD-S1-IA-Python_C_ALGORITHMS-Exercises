age=int(input("saisir l'age de l'enfant >=6: "))
if (age>=6 and age<8):
    print("le catégorie de l'enfant est Poussin.")
elif (age>=8 and age<10):
    print("le catégorie de l'enfant est Pupille.")
elif (age>=10 and age<12):
    print("le catégorie de l'enfant est Minime.")
elif (age>=12):
    print("le catégorie de l'enfant est Cadet.")
